                ResultSet result_set = statement.executeQuery(query);

                ResultSetMetaData rsmd = result_set.getMetaData();
                DefaultTableModel dtm = (DefaultTableModel) Game_Table.getModel();

                int columns = rsmd.getColumnCount();
                String[] column_name = new String[columns];

                for (int x = 0; x < columns; x++) {
                    column_name[x] = rsmd.getColumnName((x + 1));
                }

                dtm.setColumnIdentifiers(column_name);
                String gameID, gameTitle, gameDescription, developer, publisher, platform, ignRating, releaseYear, genre;

                dtm.setRowCount(0);

                while (result_set.next()) {

                    gameID = result_set.getString(1);
                    gameTitle = result_set.getString(2);
                    gameDescription = result_set.getString(3);
                    developer = result_set.getString(4);
                    publisher = result_set.getString(5);
                    platform = result_set.getString(6);
                    ignRating = result_set.getString(7);
                    releaseYear = String.valueOf(result_set.getInt(8));
                    genre = result_set.getString(9);

                    String tdata[] = {gameID, gameTitle, gameDescription, developer, publisher, platform, ignRating, releaseYear, genre};
                    dtm.addRow(tdata);
                }

                dtm.fireTableDataChanged();
                Game_Table.getParent().repaint();
                Game_Table.revalidate();

                result_set.close();
                statement.close();
                connection.close();

